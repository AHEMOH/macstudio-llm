# ===========================================================================
# Job manager (detached, disk-backed — survives our own restart)
# ===========================================================================
_jobs_lock = threading.Lock()
_job_counter = 0


def job_paths(job_id):
    return (os.path.join(JOBS_DIR, job_id + ".json"),
            os.path.join(JOBS_DIR, job_id + ".log"))


def read_job(job_id):
    jpath, lpath = job_paths(job_id)
    try:
        with open(jpath) as f:
            job = json.load(f)
    except (OSError, ValueError):
        return None
    if job.get("state") == "running":
        job = _refresh_job_state(job, lpath, jpath)
    return job


def _refresh_job_state(job, lpath, jpath):
    """Derive the live state: sentinel line in the log wins; else pid liveness."""
    rc = None
    try:
        with open(lpath, "rb") as f:
            f.seek(max(0, os.path.getsize(lpath) - 4096))
            tail = f.read().decode("utf-8", "replace")
        m = None
        for m in JOB_RC_RE.finditer(tail):
            pass
        if m:
            rc = int(m.group(1))
    except OSError:
        pass
    if rc is None:
        try:
            os.kill(job["pid"], 0)
            return job  # still running
        except (OSError, KeyError):
            rc = -1     # died without sentinel (killed / crashed)
    job["state"] = "done" if rc == 0 else "failed"
    job["rc"] = rc
    job["ended"] = int(time.time())
    try:
        with open(jpath, "w") as f:
            json.dump(job, f)
    except OSError:
        pass
    return job


def list_jobs():
    try:
        names = [n[:-5] for n in os.listdir(JOBS_DIR) if n.endswith(".json")]
    except OSError:
        return []
    jobs = [j for j in (read_job(n) for n in sorted(names, reverse=True)) if j]
    jobs.sort(key=lambda j: j.get("started", 0), reverse=True)
    return jobs


def active_job():
    for j in list_jobs():
        if j.get("state") == "running":
            return j
    return None


def prune_jobs():
    jobs = list_jobs()
    for j in jobs[JOB_KEEP:]:
        if j.get("state") == "running":
            continue
        jpath, lpath = job_paths(j["id"])
        for p in (jpath, lpath):
            try:
                os.remove(p)
            except OSError:
                pass


def start_job(kind, title, argv, meta=None):
    """Spawn `argv` fully detached; returns (job, None) or (None, error)."""
    global _job_counter
    with _jobs_lock:
        running = active_job()
        if running:
            return None, running
        _job_counter += 1
        job_id = time.strftime("%Y%m%d-%H%M%S") + f"-{_job_counter:02d}-{kind}"
        jpath, lpath = job_paths(job_id)
        cmd = " ".join(shlex.quote(a) for a in argv)
        # The /bin/sh wrapper appends the rc sentinel to the SAME log so a
        # dashboard restart mid-job can still tell how it ended.
        sh = f'{cmd}; echo "__JOB_RC__=$?"'
        env = {
            "PATH": "/usr/bin:/bin:/usr/sbin:/sbin:/opt/homebrew/bin:/usr/local/bin",
            "HOME": "/var/root",
        }
        try:
            logf = open(lpath, "ab")
        except OSError as exc:
            return None, {"error": f"job log unwritable: {exc}"}
        try:
            p = subprocess.Popen(
                ["/bin/sh", "-c", sh],
                stdout=logf, stderr=subprocess.STDOUT,
                stdin=subprocess.DEVNULL,
                start_new_session=True,   # detach: survives our kickstart
                env=env, cwd="/",
            )
        except OSError as exc:
            logf.close()
            return None, {"error": f"spawn failed: {exc}"}
        finally:
            try:
                logf.close()
            except OSError:
                pass
        job = {
            "id": job_id, "kind": kind, "title": title, "argv": argv,
            "pid": p.pid, "pgid": p.pid, "started": int(time.time()),
            "state": "running", "meta": meta or {},
        }
        try:
            with open(jpath, "w") as f:
                json.dump(job, f)
        except OSError as exc:
            return None, {"error": f"job state unwritable: {exc}"}
        prune_jobs()
        log(f"job started: {job_id} pid={p.pid} :: {cmd}")
        return job, None


def cancel_job(job_id):
    job = read_job(job_id)
    if not job:
        return False, "unbekannter Job"
    if job.get("state") != "running":
        return False, "Job läuft nicht mehr"
    try:
        os.killpg(job["pgid"], signal.SIGTERM)
    except OSError as exc:
        return False, f"killpg fehlgeschlagen: {exc}"
    log(f"job cancelled: {job_id}")
    return True, None


def read_tail_lines(path, lines, max_bytes=2 * 1024 * 1024):
    """Last N lines of a file in ONE read (initial log view). Returns the
    same shape as read_incremental with next_offset = size, so the UI can
    continue polling incrementally from the end."""
    try:
        size = os.path.getsize(path)
    except OSError:
        return {"data": "", "next_offset": 0, "size": 0, "missing": True}
    want = min(size, max(LOG_CHUNK, lines * 300), max_bytes)
    try:
        with open(path, "rb") as f:
            f.seek(size - want)
            raw = f.read(want)
    except OSError:
        return {"data": "", "next_offset": size, "size": size}
    text = raw.decode("utf-8", "replace")
    all_lines = text.splitlines(True)
    if size > want and all_lines:
        all_lines = all_lines[1:]      # drop the likely-partial first line
    return {"data": "".join(all_lines[-lines:]), "next_offset": size, "size": size}


def read_incremental(path, offset, limit=LOG_CHUNK):
    """Rotation-aware cursor read: size < offset -> reset to 0."""
    try:
        size = os.path.getsize(path)
    except OSError:
        return {"data": "", "next_offset": 0, "size": 0, "missing": True}
    if offset > size:
        offset = 0
    try:
        with open(path, "rb") as f:
            f.seek(offset)
            raw = f.read(limit)
    except OSError:
        return {"data": "", "next_offset": offset, "size": size}
    return {
        "data": raw.decode("utf-8", "replace"),
        "next_offset": offset + len(raw),
        "size": size,
    }


