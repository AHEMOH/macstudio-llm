def model_local_dir(repo, hf_cache):
    return os.path.join(hf_cache, "hub", "models--" + repo.replace("/", "--"))


def model_status(repo, hf_cache):
    d = model_local_dir(repo, hf_cache)
    snaps = os.path.join(d, "snapshots")
    has_st = False
    if os.path.isdir(snaps):
        for _root, _dirs, files in os.walk(snaps):
            if any(fn.endswith(".safetensors") for fn in files):
                has_st = True
                break
    if has_st:
        blobs = os.path.join(d, "blobs")
        if os.path.isdir(blobs):
            for _root, _dirs, files in os.walk(blobs):
                if any(fn.endswith(".incomplete") for fn in files):
                    return "partial"
        return "ok"
    return "partial" if os.path.isdir(d) else "none"


def model_downloaded_bytes(repo, hf_cache):
    """Bytes on disk under the repo's blobs dir (incl. .incomplete) — the
    live download-progress source while a --download-model job runs."""
    blobs = os.path.join(model_local_dir(repo, hf_cache), "blobs")
    total = 0
    try:
        for root, _dirs, files in os.walk(blobs):
            for fn in files:
                try:
                    total += os.path.getsize(os.path.join(root, fn))
                except OSError:
                    pass
    except OSError:
        pass
    return total


