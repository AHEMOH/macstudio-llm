def session_for(token):
    return hmac.new(token.encode("utf-8"), SESSION_SALT, hashlib.sha256).hexdigest()


class Handler(BaseHTTPRequestHandler):
    server_version = "macstudio-dashboard"
    protocol_version = "HTTP/1.1"

    def log_message(self, fmt, *args):
        pass

    # -- helpers -------------------------------------------------------------
    def send_json(self, obj, code=200, extra_headers=None):
        body = json.dumps(obj).encode("utf-8")
        self.send_response(code)
        self.send_header("Content-Type", "application/json; charset=utf-8")
        self.send_header("Content-Length", str(len(body)))
        self.send_header("Cache-Control", "no-store")
        for k, v in (extra_headers or {}).items():
            self.send_header(k, v)
        self.end_headers()
        try:
            self.wfile.write(body)
        except OSError:
            pass

    def read_body_json(self):
        try:
            n = int(self.headers.get("Content-Length", "0") or "0")
        except ValueError:
            n = 0
        if n <= 0 or n > 1024 * 1024:
            return {}
        try:
            return json.loads(self.rfile.read(n).decode("utf-8"))
        except (OSError, ValueError):
            return {}

    def cookie_value(self):
        raw = self.headers.get("Cookie", "") or ""
        for part in raw.split(";"):
            k, _, v = part.strip().partition("=")
            if k == COOKIE_NAME:
                return v
        return ""

    def authed(self):
        token = conf_get("DASHBOARD_TOKEN")
        if not token:
            return False
        auth = self.headers.get("Authorization", "") or ""
        if auth.startswith("Bearer "):
            if hmac.compare_digest(auth[7:].strip(), token):
                return True
        cookie = self.cookie_value()
        if cookie and hmac.compare_digest(cookie, session_for(token)):
            return True
        return False

    def deny(self):
        self.send_json({"error": "nicht angemeldet"}, code=401)

    def busy_409(self, job):
        self.send_json({"error": "Ein Vorgang läuft bereits", "active_job": job}, code=409)

    # -- GET -----------------------------------------------------------------
    def do_GET(self):
        parsed = urlparse(self.path)
        path = parsed.path
        qs = parse_qs(parsed.query)

        if path == "/" or path == "/index.html":
            body = ui_body()
            if body is None:
                body = ("<h1>Dashboard-UI fehlt</h1><p>dashboard-ui.html nicht gefunden — "
                        "<code>sudo bash setup.sh --apply</code> ausführen.</p>").encode("utf-8")
            self.send_response(200)
            self.send_header("Content-Type", "text/html; charset=utf-8")
            self.send_header("Content-Length", str(len(body)))
            self.send_header("Cache-Control", "no-cache")
            self.end_headers()
            try:
                self.wfile.write(body)
            except OSError:
                pass
            return

        if not path.startswith("/api/"):
            self.send_json({"error": "not found"}, code=404)
            return
        if not self.authed():
            self.deny()
            return

        try:
            offset = int(qs.get("offset", ["0"])[0])
        except ValueError:
            offset = 0

        if path == "/api/status":
            self.send_json(api_status())
        elif path == "/api/models":
            self.send_json(api_models())
        elif path == "/api/telemetry":
            self.send_json(api_telemetry())
        elif path == "/api/paperless-ocr":
            self.send_json(api_paperless_ocr())
