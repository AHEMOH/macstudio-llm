def setup_sync(args, timeout=60, stdin_text=None):
    setup = find_setup_sh()
    if not setup:
        return 1, "setup.sh nicht gefunden (macstudio.repo fehlt)"
    env = {
        "PATH": "/usr/bin:/bin:/usr/sbin:/sbin:/opt/homebrew/bin:/usr/local/bin",
        "HOME": "/var/root",
    }
    try:
        p = subprocess.run(
            ["/bin/bash", setup] + args,
            capture_output=True, text=True, timeout=timeout,
            input=stdin_text, env=env, cwd="/",
        )
    except subprocess.TimeoutExpired:
        return 1, f"Timeout nach {timeout} s"
    except OSError as exc:
        return 1, str(exc)
    out = (p.stdout or "") + (p.stderr or "")
    return p.returncode, out.strip()


_schema_cache = {"ts": 0.0, "data": None}


def config_schema():
    now = time.time()
    if _schema_cache["data"] is not None and now - _schema_cache["ts"] < SCHEMA_CACHE_SEC:
        return _schema_cache["data"]
    rc, out = setup_sync(["--config-schema"], timeout=30)
    if rc != 0:
        return _schema_cache["data"] or []
    rows = []
    for line in out.splitlines():
        parts = line.split("\t")
        if len(parts) < 2 or not parts[0]:
            continue
        parts += [""] * (4 - len(parts))
        rows.append({"key": parts[0], "value": parts[1],
                     "default": parts[2], "hint": parts[3]})
    _schema_cache["ts"] = now
    _schema_cache["data"] = rows
    return rows


def invalidate_schema_cache():
    _schema_cache["ts"] = 0.0
    _schema_cache["data"] = None


