_conf_cache = {"mtime": 0.0, "data": {}}
_conf_lock = threading.Lock()


def conf():
    with _conf_lock:
        try:
            mt = os.stat(CONF_FILE).st_mtime
        except OSError:
            return dict(_conf_cache["data"])
        if mt != _conf_cache["mtime"]:
            data = {}
            try:
                with open(CONF_FILE) as f:
                    for line in f:
                        line = line.strip()
                        if not line or line.startswith("#") or "=" not in line:
                            continue
                        k, _, v = line.partition("=")
                        k, v = k.strip(), v.strip()
                        if len(v) >= 2 and v[0] == v[-1] and v[0] in ("'", '"'):
                            v = v[1:-1]
                        data[k] = v
            except OSError:
                data = dict(_conf_cache["data"])
            _conf_cache["mtime"] = mt
            _conf_cache["data"] = data
        return dict(_conf_cache["data"])


