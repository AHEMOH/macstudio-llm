#!/usr/bin/env python3
"""
Web dashboard for the MacStudio LLM Server — browser control of models,
services, settings, logs and telemetry. The SSH TUI (setup.sh) stays fully
authoritative; this daemon only shells out to setup.sh CLI verbs and to
whitelisted launchctl actions — it never reimplements bash logic.

Stdlib only (runs on /usr/bin/python3 as the launchd daemon
com.local.dashboard, as root — it reads `launchctl print system/*`, kickstarts
daemons and spawns setup.sh). ThreadingHTTPServer on 0.0.0.0:DASHBOARD_PORT.

Key design points (see CLAUDE.md):
  * Mutating jobs (--apply, --set-model, --download-model) run as DETACHED
    processes (start_new_session) with file-backed state under
    /var/log/macstudio/dashboard-jobs/ — a dashboard-triggered --apply
    restarts THIS daemon (service_py_label hot-restart / plist reload), so the
    job must not be our child-with-pipes. The plist sets AbandonProcessGroup.
    Job completion is detected via a `__JOB_RC__=<rc>` sentinel the /bin/sh
    wrapper appends to the job log. One mutating job at a time (409 else).
  * No SSE — cursor polling. /api/logs/<name>?offset=N and
    /api/jobs/<id>?offset=N return incremental bytes; the UI polls. Survives
    the self-restart above, no thread-lifecycle issues.
  * Auth: DASHBOARD_TOKEN from macstudio.conf (auto-generated by --apply).
    Login sets a stateless HMAC cookie derived from the token, so rotating
    the token invalidates every browser without a daemon restart. All /api/*
    (except /api/login) need the cookie or `Authorization: Bearer <token>`.
  * Telemetry: scrapes the silicon exporter (:9101) when INSTALL_EXPORTERS=1,
    and always samples the cheap basics itself (vm_stat/sysctl/ioreg). Ring
    buffer of ~1 h so charts fill instantly on page load.

Config via env (wrappers/start-dashboard.sh) + live re-reads of macstudio.conf:
  DASHBOARD_PORT   (the token is deliberately NOT passed via env — it is
                    re-read from the conf per request so rotation is instant)
"""
