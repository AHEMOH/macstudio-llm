            if not full.startswith(os.path.realpath(LOG_DIR) + os.sep):
                self.send_json({"error": "ungültiger Pfad"}, code=400)
                return
            # No offset -> initial view: last N lines in one shot (default
            # 1000). With offset -> incremental follow-up read.
